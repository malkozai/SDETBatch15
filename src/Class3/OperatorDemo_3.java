package Class3;

public class OperatorDemo_3 {
    public static void main(String[] args) {

        /*
        Operators: Operators are different symbols that we can use to perform operations like
        addition, subtractions we can compare things if two entities are same or not if a number
        is greater than the other number or not.
        There are three main types of operators.
        They are Arithmetic operators, Relational operators and logical operators.
         */

        int number = 10;
        System.out.println(number/3);
        System.out.println(number%3);

        System.out.println(16/5);
        System.out.println(16%5);
        System.out.println(156%2);
        System.out.println(155%2);// we always take the modulus (%) by 2 if the answer is 0 it is an even number
        // otherwise it is an odd number
        System.out.println(1555%5);
    }
}
