package Class6;

public class WhileLoop3 {
    public static void main(String[] args) {

            int number = 10;
            while (number>0) { // keeps on checking the condition as long as it returns ture also if we put (true) inside while bracket then it will print infinite time or keep printing
                System.out.println(number);
                number--; // decrement the number
            }
    }
}

