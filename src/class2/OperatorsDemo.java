package class2;

public class OperatorsDemo {
    public static void main(String[] args) {

        int numberA=10;
      //  int number=20; we can't name two boxes with the same name it's not allowed
        System.out.println(numberA);

        int number1= 10;
        int number2=20;
        System.out.println(number1+number2); // used for addition
        System.out.println(number1-number2); // used for subtraction
        System.out.println(number1*number2); // used for multiplication
        System.out.println(number1/number2);   // used for deviation

        /*
        if we want to store whole number we should use (int) datatype
        if we want to store decimal number we should use (double) datatype
        if we want to store characters we should use (String) datatype

         */

    }
}

